#include <stdio.h>

int main(void) {
    int a;

    // 核心：do 块负责执行输入和提示
    do {
        printf("输入一个介于 1 到 100 之间的整数: ");
        
        // 读取输入
        if (scanf("%d", &a) != 1) {
            printf("输入无效，请重新输入。\n");
            // 清除输入缓冲区，防止下次循环继续读取错误字符
            while (getchar() != '\n'); 
            a = 0; // 强制 a 为无效值，确保 while 条件检查正确
            continue; // 跳过本次循环的剩余部分
        }

        // 如果输入无效，给用户错误提示
        if (a < 1 || a > 100) {
            printf("输入错误！数字必须在 1 到 100 之间。\n");
        }

    //  while 条件：只要输入是无效的 (小于1 或 大于100)，就继续循环
    } while (a < 1 || a > 100);

    // 循环退出时，表示输入有效
    printf("输入有效，你输入的是: %d\n", a);
    
    return 0;
}
