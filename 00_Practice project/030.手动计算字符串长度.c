#include <stdio.h>

int main(void) {
    //  修正 1: 索引 i 必须初始化为 0
    int i = 0;
    char word[] = "Programming";

    // 计数器不需要独立定义，我们可以直接用 i 来统计长度

    printf("字符串: %s\n", word);

    // 循环条件：只要当前索引处的字符不是空字符 '\0'，就继续
    while (word[i] != '\0') {

        //  修正 2: 在循环体内，只增加计数器 i
        // 每执行一次循环，i 就会增加 1，i 最终的值就是字符串的长度。
        i = i + 1;
        // 简写：i++;
    }
    // 循环终止时，i 的值刚好是字符的个数（因为 i 是从 0 开始计数的）

    // 循环结束后，i 就是字符串的长度
    printf("手动计算的字符串长度是: %d\n", i);

    return 0;
}
