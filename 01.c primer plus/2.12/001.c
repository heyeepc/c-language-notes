#include <stdio.h>

int main(void) {
    
    const char *first_name = "Gustav";
    const char *last_name = "Mahler";

    // ---------------------------------------------
    // 1. 第一次调用 printf()：姓名打印在一行
    // ---------------------------------------------
    printf("%s %s\n", first_name, last_name);
    // 关键点:
    // - 使用一个 printf() 调用完成任务。
    // - 最后的 \n 确保输出完后光标移到下一行。


    // ---------------------------------------------
    // 2. 第二次调用 printf()：姓名分别打印在两行
    // ---------------------------------------------
    printf("%s\n%s\n", first_name, last_name);
    // 关键点:
    // - 仍然只调用了一次 printf()。
    // - 在打印第一个名字后，使用 \n 强制换行，
    //   让第二个名字从新的一行开始打印。


    // ---------------------------------------------
    // 3. 第三次和第四次调用 printf()：姓名打印在一行
    // ---------------------------------------------
    printf("%s ", first_name); // 第三次调用
    printf("%s\n", last_name);  // 第四次调用
    // 关键点:
    // - 第三次 printf() 打印第一个名字和空格，但**没有 \n**，
    //   所以光标停留在同一行。
    // - 第四次 printf() 紧接着打印第二个名字，并在结尾使用 \n 换行。

    return 0;
}
